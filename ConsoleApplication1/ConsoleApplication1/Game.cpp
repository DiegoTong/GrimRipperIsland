void Game::gameLoop(string pName)
{
	Player player;
	player.init(pName);
	Level1 level1;
	Level2 level2;
	Level3 level3;
	level1.init(&player);
	level2.init(&player);
	level3.init(&player);
	levels.push_back(&level1);
	levels.push_back(&level2);
	levels.push_back(&level3);
	Encounter encounter;
	encounter.init();
	encounter.player = &player;
	gameOver = false;
	icheckInput = false;
	player.story1();
	while (gameOver != true)
	{	
		icheckInput = false;
		printMap();
		chooseLevel();
		if (icheckInput == true)
		{
			player.playerLocation.x = levels[levelChoice - 49]->currentRoom->sizeX / 2;
			player.playerLocation.y = levels[levelChoice - 49]->currentRoom->sizeY / 2;
			while (chosenLevel->levelFinished == false)
			{
				chosenLevel->update(levelChoice - 48);
				player.bossID = levelChoice - 48;
				player.update(chosenLevel->currentRoom->sizeX, chosenLevel->currentRoom->sizeY);
				encounter.update();
			}
			checkGameOver(player, level1, level2, level3);
		}
		system("CLS");
	}
	if (player.health > 0)
	{
		if (player.GoodDeeds == 3)
		{
			player.goodEnding();
		}
		else
		{
			player.evilEnding();
		}
		encounter.setEnemy(14);
		player.isFightingBoss = true;
		player.bossID = 4;
		encounter.battle();
		player.end();
	}

}
void Game::chooseLevel()
{
	for (int i = 0; i < 3; i++)
	{
		if (levels[i]->levelFinished == false)
		{
			if (i == 0)
			{
				cout << "Press 1 to load Mermaid Island "<<endl;
			}
			else if (i == 1)
			{
				cout << "Press 2 to load Fairy Island " << endl;
			}
			else if(i == 2)
			{
				cout << "Press 3 to load Minotaur Island " << endl;
			}
		}
	}
	cout << endl;
	levelChoice = _getch();
	switch (levelChoice-48)
	{
	case LEVEL1:
		if (levels[0]->levelFinished == false)
		{
			chosenLevel = levels[0];
			icheckInput = true;
		}
		break;
	case LEVEL2:
		if (levels[1]->levelFinished == false)
		{
			chosenLevel = levels[1];
			icheckInput = true;
		}
		break;
	case LEVEL3:
		if (levels[2]->levelFinished == false)
		{
			chosenLevel = levels[2];
			icheckInput = true;
		}
		break;
	default:

		break;
	}
}

